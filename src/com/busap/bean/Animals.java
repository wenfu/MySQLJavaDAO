package com.busap.bean;

public class Animals {
	private int id;
	private String name;
	private int age;
	private int anId;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAnId() {
		return anId;
	}

	public void setAnId(int anId) {
		this.anId = anId;
	}

}
